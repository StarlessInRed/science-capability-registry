def _matlab_string(value: str) -> str:
    return str(value).replace("'", "''")


def _env_path_check(env_name: str, required: bool, path_kind: str) -> dict[str, Any]:
    value = os.environ.get(env_name)
    configured = bool(value)
    exists = False
    if configured:
        path = Path(str(value))
        exists = path.is_file() if path_kind == "file" else path.is_dir()
    return {
        "env": env_name,
        "required": required,
        "path_kind": path_kind,
        "configured": configured,
        "exists": exists,
    }


def _collect_environment(config: dict[str, Any]) -> list[dict[str, Any]]:
    return [
        _env_path_check(config["matlab"]["executable_env"], True, "file"),
        _env_path_check(config["comsol"]["command_env"], True, "file"),
        _env_path_check(config["livelink"]["mli_dir_env"], True, "directory"),
        _env_path_check(config["application_library"]["root_env"], True, "directory"),
        _env_path_check(config["comsol"]["mphserver_env"], False, "file"),
    ]


def _relative_parts(path_value: str) -> list[str]:
    return [part for part in str(path_value).replace("\\", "/").split("/") if part]


def _source_path(root: Path, relative_path: str) -> Path:
    return root.joinpath(*_relative_parts(relative_path))


def _source_status(config: dict[str, Any]) -> list[dict[str, Any]]:
    root_value = os.environ.get(config["application_library"]["root_env"])
    source_paths = [config["replay"]["source_script"], config["replay"]["model_file"]]
    if not root_value:
        return [
            {"relative_path": item, "configured": False, "exists": False}
            for item in source_paths
        ]
    root = Path(root_value)
    return [
        {
            "relative_path": item,
            "configured": True,
            "exists": _source_path(root, item).is_file(),
        }
        for item in source_paths
    ]


def _matlab_path_expr(relative_path: str) -> str:
    parts = ", ".join(f"'{_matlab_string(part)}'" for part in _relative_parts(relative_path))
    return f"fullfile(appRoot, {parts})"


