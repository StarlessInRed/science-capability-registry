def _script_text(config: dict[str, Any]) -> str:
    lines = _script_header(config)
    if config["replay"]["candidate_id"] == "livelink_domain_activation":
        lines.extend(_domain_activation_lines(config))
    else:
        lines.extend(_pseudoperiodicity_lines(config))
    lines.append("try")
    lines.append("    ModelUtil.remove(model.tag);")
    lines.append("catch")
    lines.append("end")
    lines.append("")
    return "\n".join(lines)


def _write_script(config: dict[str, Any], output_dir: Path) -> Path:
    script_path = output_dir / config["replay"]["script_filename"]
    script_path.write_text(_script_text(config), encoding="ascii")
    return script_path


def _write_placeholders(output_dir: Path, message: str) -> None:
    for name in ["stdout.txt", "stderr.txt"]:
        (output_dir / name).write_text(message + "\n", encoding="utf-8")


def _write_dry_run_artifacts(config: dict[str, Any], output_dir: Path) -> None:
    for name in config["outputs"]["expected_outputs"]:
        if name in {
            config["replay"]["script_filename"],
            "stdout.txt",
            "stderr.txt",
            "metrics.json",
            "validation.json",
            "validation_report.md",
            "manifest.json",
        }:
            continue
        target = output_dir / name
        if target.suffix.lower() == ".csv":
            target.write_text("probe,expression,value,unit\n", encoding="utf-8")
        else:
            payload = {
                "capability_id": config["capability_id"],
                "case_id": config["case_id"],
                "candidate_id": config["replay"]["candidate_id"],
                "artifact_path": name,
                "runtime_executed": False,
                "official_replay_script_generated": True,
                "no_claims": config["validation"]["no_claims"],
            }
            target.write_text(json.dumps(payload, indent=2), encoding="utf-8")


def _execute_matlab(config: dict[str, Any], output_dir: Path, script_path: Path) -> int:
    matlab_exe = Path(os.environ[config["matlab"]["executable_env"]])
    stdout_path = output_dir / "stdout.txt"
    stderr_path = output_dir / "stderr.txt"
    script_expr = f"run('{script_path.resolve().as_posix()}')"
    with (
        stdout_path.open("w", encoding="utf-8", errors="replace") as stdout,
        stderr_path.open("w", encoding="utf-8", errors="replace") as stderr,
    ):
        result = subprocess.run(
            [str(matlab_exe), config["matlab"]["batch_argument"], script_expr],
            cwd=output_dir,
            stdout=stdout,
            stderr=stderr,
            timeout=float(config["backend"]["timeout_s"]),
            check=False,
        )
    return result.returncode


def _load_json(path: Path) -> dict[str, Any] | None:
    if not path.exists():
        return None
    data = json.loads(path.read_text(encoding="utf-8"))
    if isinstance(data, dict):
        return data
    return None


def _probe_rows(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def _finite_fraction(rows: list[dict[str, str]]) -> float:
    if not rows:
        return 0.0
    finite_count = 0
    for row in rows:
        try:
            value = float(row.get("value", "nan"))
        except ValueError:
            continue
        if math.isfinite(value):
            finite_count += 1
    return finite_count / len(rows)


def _artifact_exists(output_dir: Path, name: str) -> bool:
    path = output_dir / name
    if name in {"stdout.txt", "stderr.txt"}:
        return path.exists()
    return path.exists() and path.stat().st_size > 0


def _build_metrics(
    config: dict[str, Any],
    output_dir: Path,
    script_path: Path,
    environment_checks: list[dict[str, Any]],
    dry_run: bool,
    matlab_return_code: int | None,
) -> dict[str, Any]:
    env_summary = summarize_environment_checks(environment_checks)
    source_status = _source_status(config)
    selection = _load_json(output_dir / "selection_map.json")
    physics = _load_json(output_dir / "physics_assignment_manifest.json")
    boundary = _load_json(output_dir / "boundary_assignment_manifest.json")
    solver = _load_json(output_dir / "solver_manifest.json")
    dataset = _load_json(output_dir / "dataset_manifest.json")
    export = _load_json(output_dir / "export_manifest.json")
    units = _load_json(output_dir / "units.json")
    rows = _probe_rows(output_dir / "probes.csv")
    expected_written = all(
        _artifact_exists(output_dir, name) for name in config["replay"]["expected_outputs"]
    )
    if dry_run:
        runtime_status = "dry_run_not_executed"
    elif matlab_return_code == 0 and expected_written:
        runtime_status = "matlab_livelink_application_library_replay_passed"
    else:
        runtime_status = "matlab_livelink_application_library_replay_failed"
    return {
        "validated_config": True,
        "script_generated": script_path.exists(),
        "script_file": script_path.name,
        "runtime_status": runtime_status,
        "matlab_executed": matlab_return_code is not None,
        "matlab_return_code": matlab_return_code,
        "environment_summary": env_summary,
        "source_file_count": len(source_status),
        "source_files_existing_count": sum(1 for item in source_status if item["exists"]),
        "source_files": source_status,
        "selection_role_count": int(selection.get("selection_role_count", 0)) if selection else 0,
        "physics_created": bool(physics and physics.get("physics_created")),
        "missing_boundary_assignment_count": int(boundary.get("missing_boundary_assignment_count", 1)) if boundary else 1,
        "solver_completed": bool(solver and solver.get("solver_completed")),
        "dataset_count": int(dataset.get("dataset_count", 0)) if dataset else 0,
        "exported_probe_count": int(export.get("exported_probe_count", len(rows))) if export else len(rows),
        "finite_value_fraction": _finite_fraction(rows),
        "missing_unit_count": int(units.get("missing_unit_count", 1)) if units else 1,
        "covers_capability_count": len(config["covers_capabilities"]),
    }


def _add_check(checks: list[dict[str, Any]], name: str, passed: bool, details: str) -> None:
    checks.append({"name": name, "passed": bool(passed), "details": details})


def validate_application_library_metrics(
    metrics: dict[str, Any],
    config: dict[str, Any],
    output_dir: str | Path,
    check_artifacts: bool = True,
) -> dict[str, Any]:
    checks: list[dict[str, Any]] = []
    dry_run = metrics["runtime_status"] == "dry_run_not_executed"
    env_summary = metrics["environment_summary"]
    _add_check(checks, "config.validated", bool(metrics["validated_config"]), "configuration schema accepted")
    _add_check(checks, "script.generated", bool(metrics["script_generated"]), metrics["script_file"])
    _add_check(checks, "capabilities.covered", metrics["covers_capability_count"] == 4, str(metrics["covers_capability_count"]))
    if not dry_run:
        _add_check(
            checks,
            "environment.required_configured",
            bool(env_summary["all_required_configured"]),
            f"{env_summary['required_configured_count']} of {env_summary['required_count']}",
        )
        if config["validation"]["require_path_exists"]:
            _add_check(
                checks,
                "environment.required_paths_exist",
                bool(env_summary["all_required_paths_exist"]),
                f"{env_summary['required_existing_count']} of {env_summary['required_count']}",
            )
        _add_check(
            checks,
            "application_library.source_files_exist",
            metrics["source_files_existing_count"] == metrics["source_file_count"],
            f"{metrics['source_files_existing_count']} of {metrics['source_file_count']}",
        )
        if config["validation"]["require_matlab_execution"]:
            _add_check(checks, "matlab.return_code", metrics.get("matlab_return_code") == 0, str(metrics.get("matlab_return_code")))
        _add_check(checks, "selection.roles_declared", metrics["selection_role_count"] >= 1, str(metrics["selection_role_count"]))
        _add_check(checks, "physics.created", bool(metrics["physics_created"]), str(metrics["physics_created"]))
        _add_check(checks, "boundary.assignments_complete", metrics["missing_boundary_assignment_count"] == 0, str(metrics["missing_boundary_assignment_count"]))
        _add_check(checks, "solver.completed", bool(metrics["solver_completed"]), str(metrics["solver_completed"]))
        _add_check(checks, "dataset.present", metrics["dataset_count"] >= 1, str(metrics["dataset_count"]))
        _add_check(checks, "probe.exported", metrics["exported_probe_count"] >= 1, str(metrics["exported_probe_count"]))
        _add_check(checks, "probe.values_finite", metrics["finite_value_fraction"] >= 1.0, str(metrics["finite_value_fraction"]))
        _add_check(checks, "units.present", metrics["missing_unit_count"] == 0, str(metrics["missing_unit_count"]))
    if check_artifacts:
        for rel_path in config["validation"]["required_artifacts"]:
            _add_check(
                checks,
                f"artifact.exists.{rel_path}",
                _artifact_exists(Path(output_dir), rel_path),
                rel_path,
            )
    return {
        "passed": all(item["passed"] for item in checks),
        "gate": "static-readiness" if dry_run else config["validation"]["gate"],
        "scope": (
            "COMSOL official Application Library replay script dry run; MATLAB and COMSOL were not executed"
            if dry_run
            else "COMSOL official Application Library MATLAB LiveLink replay smoke"
        ),
        "checks": checks,
        "details": {
            "runtime_status": metrics["runtime_status"],
            "candidate_id": config["replay"]["candidate_id"],
            "no_claims": config["validation"]["no_claims"],
        },
    }


def _write_report(path: str | Path, config: dict[str, Any], metrics: dict[str, Any], validation: dict[str, Any]) -> None:
    status = "passed" if validation["passed"] else "failed"
    lines = [
        f"# COMSOL {config['case_id']} validation report",
        "",
        f"- gate: {validation['gate']}",
        f"- status: {status}",
        f"- candidate: {config['replay']['candidate_id']}",
        f"- runtime status: {metrics['runtime_status']}",
        f"- source files existing: {metrics['source_files_existing_count']} / {metrics['source_file_count']}",
        f"- MATLAB return code: {metrics['matlab_return_code']}",
        "",
        "## Checks",
        "",
    ]
    for item in validation["checks"]:
        mark = "PASS" if item["passed"] else "FAIL"
        lines.append(f"- {item['name']}: {mark}")
    lines.extend(["", "## Scope", "", validation["scope"]])
    lines.extend(["", "## No Claims", ""])
    for claim in config["validation"]["no_claims"]:
        lines.append(f"- {claim}")
    Path(path).write_text("\n".join(lines) + "\n", encoding="utf-8")


def run(
    config_path: str | Path | None = None,
    config: dict[str, Any] | None = None,
    output_dir: str | Path | None = None,
    dry_run: bool = False,
    backend: str | None = None,
) -> dict[str, Any]:
    if config is None:
        if config_path is None:
            raise ValueError("Either config_path or config must be provided.")
        config = load_static_config(config_path, SCHEMA_PATH)
    else:
        config = validate_static_config(config, SCHEMA_PATH)
    if backend is not None:
        config = {**config, "backend": {**config["backend"], "type": backend}}
        config = validate_static_config(config, SCHEMA_PATH)
    backend_type = config["backend"]["type"]
    if backend_type not in {"dry_run_only", "matlab_livelink_application_library_replay_smoke"}:
        raise NotImplementedError(f"COMSOL Application Library backend {backend_type!r} is not implemented.")
    output_path = Path(output_dir) if output_dir is not None else repo_relative_path(config["outputs"]["output_dir"])
    output_path.mkdir(parents=True, exist_ok=True)
    script_path = _write_script(config, output_path)
    environment_checks = _collect_environment(config)
    env_summary = summarize_environment_checks(environment_checks)
    matlab_return_code: int | None = None
    generated_files = [
        script_path.name,
        "stdout.txt",
        "stderr.txt",
        "metrics.json",
        "validation.json",
        "validation_report.md",
        "manifest.json",
    ]
    if dry_run or backend_type == "dry_run_only":
        _write_placeholders(output_path, "dry-run placeholder; MATLAB and COMSOL were not executed.")
        _write_dry_run_artifacts(config, output_path)
        generated_files.extend(
            name
            for name in config["outputs"]["expected_outputs"]
            if name not in generated_files
        )
    elif env_summary["all_required_paths_exist"]:
        matlab_return_code = _execute_matlab(config, output_path, script_path)
        for name in config["replay"]["expected_outputs"]:
            if (output_path / name).exists():
                generated_files.append(name)
    else:
        _write_placeholders(output_path, "runtime profile is incomplete; MATLAB and COMSOL were not executed.")

    metrics = _build_metrics(config, output_path, script_path, environment_checks, dry_run or backend_type == "dry_run_only", matlab_return_code)
    validation = validate_application_library_metrics(metrics, config, output_path, check_artifacts=False)
    manifest = {
        "capability_id": config["capability_id"],
        "asset_id": config["asset_id"],
        "case_id": config["case_id"],
        "source_config": config.get("_config_path"),
        "schema_id": SCHEMA_ID,
        "validated_config": True,
        "output_dir": str(output_path),
        "covers_capabilities": config["covers_capabilities"],
        "backend": config["backend"],
        "replay": config["replay"],
        "generated_files": generated_files,
        "expected_outputs": config["outputs"]["expected_outputs"],
        "metrics": metrics,
        "validation": validation,
        "scope": validation["scope"],
    }
    (output_path / "metrics.json").write_text(json.dumps(metrics, indent=2), encoding="utf-8")
    (output_path / "validation.json").write_text(json.dumps(validation, indent=2), encoding="utf-8")
    _write_report(output_path / "validation_report.md", config, metrics, validation)
    (output_path / "manifest.json").write_text(json.dumps(manifest, indent=2), encoding="utf-8")

